create table theater(
theaterid integer not null,
zipcode varchar(10),
theatername varchar(50),
address varchar(50),
city varchar(50),
states varchar(50),
primary key (theaterid)
);
create table movies(
movieid integer not null,
title varchar(50),
genre varchar(50),
description varchar(500),
releasedate varchar(50),
rating varchar(50),
runtime varchar(50),
casts varchar(500),
poster varchar(200) not null,
primary key (movieid)
);
create table theatermovie(
theaterid integer not null,
movieid integer not null,
primary key(theaterid,movieid)
);
create table Screening(
screeningid integer not null,
theaterid integer not null,
movieid integer not null,
showtime varchar(10) not null,
screen varchar(20),
auditorium varchar(25),
primary key(screeningid,movieid)
);

create table reservation(
reservationid integer not null generated always as identity (start with 13473,increment by 100),
customerFname varchar(25),
customerLname varchar(25),
customerEmail varchar(50),
purchaseTimeDate varchar(50),
 primary key (reservationid)
);

